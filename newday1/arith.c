/*program explains arithmetic operation
Usage:operator-(+,-,*,/,%)
Process-a+b */
#include<stdio.h>
int main()
{
    int a=12+10;
    int b=12-10;
    int c=12*10;
    float d=12/10;
    int e=12%10;
    printf("a+b=%d\n",a);
    printf("a-b=%d\n",b);
    printf("a*b=%d\n",c);
    printf("a/b=%f\n",d);
    printf("a%b=%d\n",e);
}